"The sheer scale and bare functionality of the structures on the base can illustrate for present and future generations, in a way no document can, the reality of the struggle with Soviet Communism," he said.<br>

"In our view much of this character would be lost by future ill-thought-out change and there stands an opportunity to ensure this does not happen.

